const readings = JSON.parse(readFileSync(join(here, 'readings.json'), 'utf8'))

const missing = []
const out = readings.map((r) => {
  const gloss = M[r.kanji]
  if (!gloss) missing.push(r.kanji)
  const meaning = gloss ?? '(meaning pending)'
  const meanings = Array.from(
    new Set(
      meaning
        .split(',')
        .map((s) => s.trim().toLowerCase().replace(/\s*\(.*?\)\s*/g, '').trim())
        .filter(Boolean),
    ),
  )
  return { id: r.id, kanji: r.kanji, meaning, meanings }
})

writeFileSync(join(here, 'meanings.json'), JSON.stringify(out))
console.log(`✓ Wrote ${out.length} meanings → scripts/meanings.json`)
if (missing.length) console.warn(`⚠  Missing glosses for: ${missing.join(' ')}`)
else console.log('✓ Every kanji has a gloss.')
